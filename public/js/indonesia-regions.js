// Helper functions
function getProvinces() {
  return Object.keys(INDONESIA_REGIONS);
}

function getCities(province) {
  if (!province || !INDONESIA_REGIONS[province]) return [];
  return Object.keys(INDONESIA_REGIONS[province].cities);
}

function getDistricts(province, city) {
  if (!province || !city || !INDONESIA_REGIONS[province]?.cities[city]) return [];
  return Object.keys(INDONESIA_REGIONS[province].cities[city].districts);
}

function getVillages(province, city, district) {
  if (!province || !city || !district || !INDONESIA_REGIONS[province]?.cities[city]?.districts[district]) return [];
  return INDONESIA_REGIONS[province].cities[city].districts[district].villages || [];
}

